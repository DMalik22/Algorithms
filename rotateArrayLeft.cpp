vector<int> array_left_rotation(vector<int> a, int n, int k) {
    vector<int> to_ret (n);
    for(int i = 0; i < a.size(); i++){
        to_ret[i] = a[(k + i) % n];
    }
    return to_ret;
}

int main(){
    int n;
    int k;
    cin >> n >> k;
    vector<int> a(n);
    for(int a_i = 0;a_i < n;a_i++){
        cin >> a[a_i];
    }
    vector<int> output = array_left_rotation(a, n, k);
    for(int i = 0; i < n;i++)
        cout << output[i] << " ";
    cout << endl;
    return 0;
}
